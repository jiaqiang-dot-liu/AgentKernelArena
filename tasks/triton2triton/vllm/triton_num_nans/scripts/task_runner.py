os.chdir(TASK_DIR)

TASK_NAME = "triton2triton/triton_num_nans"
SOURCE_FILE = os.path.join(TASK_DIR, "source", "triton_num_nans.py")

# (num_reqs, vocab_size)
TEST_SHAPES = [
    (4, 1024),
    (8, 4096),
    (16, 8192),
    (32, 32000),
    (64, 65536),
]
WARMUP_ITERATIONS = 10
BENCHMARK_ITERATIONS = 100


